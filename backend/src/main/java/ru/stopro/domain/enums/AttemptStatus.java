package ru.stopro.domain.enums;

/**
 * Статус попытки решения
 */
public enum AttemptStatus {
    /**
     * В процессе - ученик ещё решает
     */
    IN_PROGRESS,
    
    /**
     * Отправлено - ответ отправлен, ожидает проверки
     */
    SUBMITTED,
    
    /**
     * Проверяется автоматически
     */
    AUTO_CHECKING,
    
    /**
     * Требует ручной проверки
     */
    NEEDS_REVIEW,
    
    /**
     * Проверено (автоматически или вручную)
     */
    CHECKED,
    
    /**
     * Завершено
     */
    COMPLETED,
    
    /**
     * Прервано (таймаут, закрытие браузера)
     */
    INTERRUPTED,
    
    /**
     * Отклонено (плагиат, нарушение)
     */
    REJECTED,
    
    /**
     * Пропущено
     */
    SKIPPED,
    
    /**
     * Время истекло
     */
    TIMED_OUT,

    /**
     * Таймаут (для совместимости)
     */
    TIMEOUT
}

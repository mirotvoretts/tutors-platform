package ru.stopro.domain.enums;

/**
 * Тип назначенного задания
 */
public enum AssignmentType {
    /**
     * Домашнее задание
     * Без ограничения времени, несколько попыток
     */
    HOMEWORK,
    
    /**
     * Тест
     * Ограничение по времени, обычно 1 попытка
     */
    TEST,
    
    /**
     * Контрольная работа
     * Строгие ограничения, учитывается в итоговой оценке
     */
    CONTROL_WORK,
    
    /**
     * Пробный экзамен (симуляция ЕГЭ)
     * Полный формат ЕГЭ с ограничением времени
     */
    MOCK_EXAM,
    
    /**
     * Тренировка
     * Без ограничений, с подсказками
     */
    PRACTICE,
    
    /**
     * Самостоятельная работа
     * Для закрепления материала
     */
    SELF_STUDY,
    
    /**
     * Вступительная диагностика
     * Для определения уровня ученика
     */
    DIAGNOSTIC,
    
    /**
     * Итоговый тест
     * По результатам изучения темы/модуля
     */
    FINAL_TEST
}

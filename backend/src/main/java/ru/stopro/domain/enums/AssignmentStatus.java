package ru.stopro.domain.enums;

/**
 * Статус назначенного задания
 */
public enum AssignmentStatus {
    /**
     * Черновик - не опубликовано
     */
    DRAFT,
    
    /**
     * Запланировано - опубликуется автоматически в start_date
     */
    SCHEDULED,
    
    /**
     * Опубликовано - доступно для выполнения
     */
    PUBLISHED,
    
    /**
     * В процессе - есть начавшие, но не все завершили
     */
    IN_PROGRESS,
    
    /**
     * Просрочено - дедлайн прошёл, но не все выполнили
     */
    OVERDUE,
    
    /**
     * Выполнено - все ученики сдали
     */
    COMPLETED,
    
    /**
     * На проверке - требуется ручная проверка
     */
    CHECKING,
    
    /**
     * Проверено - все работы проверены
     */
    CHECKED,
    
    /**
     * Архивировано
     */
    ARCHIVED,
    
    /**
     * Отменено
     */
    CANCELLED,

    /**
     * Активно (для совместимости)
     */
    ACTIVE
}
